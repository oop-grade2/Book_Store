    private void clearEmptyRows() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
    }

    private void addBookToTable(String bookId, int qty) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        BookDAOimp bookDAO = new BookDAOimp(DBConnection.getConnection());
        String title = bookDAO.getBookTitleById(bookId);
BigDecimal priceBD = bookDAO.getBookPriceById(bookId);
double price = priceBD.doubleValue();

        double total = price * qty;

        model.addRow(new Object[]{
            bookId,
            title,
            price,
            qty,
            total
        });
    }

    private void updateTotalPrice() {
    double grandTotal = 0;
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

    for (int i = 0; i < model.getRowCount(); i++) {

        Object priceObj = model.getValueAt(i, 2); // Price
        Object qtyObj   = model.getValueAt(i, 3); // Quantity

        if (priceObj != null && qtyObj != null) {

            double price = Double.parseDouble(priceObj.toString());
            int qty = Integer.parseInt(qtyObj.toString());

            double rowTotal = price * qty;
            model.setValueAt(rowTotal, i, 4);

            grandTotal += rowTotal;
        }
    }

    jLabel3.setText("Total Price: " + grandTotal + " LE");
}

    
    private void calculateTotal() {
        double sum = 0;
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        for (int i = 0; i < model.getRowCount(); i++) {
            Object val = model.getValueAt(i, 4); // Total column
            if (val != null) {
                sum += Double.parseDouble(val.toString());
            }
        }
        jLabel3.setText("Total Price: " + sum + " LE");
    }


    
   
   private void addQuantityListener() {
    jTable1.getModel().addTableModelListener(e -> {
        if (e.getColumn() == 3) { // Quantity
            updateTotalPrice();
        }
    });
}

   
    
