    try {
        // 1️⃣ إنشاء الأوردر
        Orders order = new Orders();
        order.setOrderDate(java.time.LocalDate.now());
        order.setPaymentStatus(
                jComboBox1.getSelectedItem().toString()
        );

        // 2️⃣ تحديد اليوزر 
        Customer customer = Session.getCurrentCustomer();

if (customer == null) {
    JOptionPane.showMessageDialog(this, "Please login first");
    return;
}

order.setCustomer(customer);


        // 3️⃣ قراءة بيانات الجدول
        DefaultTableModel model =
                (DefaultTableModel) jTable1.getModel();

        for (int i = 0; i < model.getRowCount(); i++) {
            Object bookIdObj = model.getValueAt(i, 0);
            Object priceObj  = model.getValueAt(i, 2);
            Object qtyObj    = model.getValueAt(i, 3);

            if (bookIdObj == null) continue;

            OrderItems item = new OrderItems();
//            item.setBookId(bookIdObj.toString());
            Book book = new Book();
             book.setBookId(bookIdObj.toString());
            item.setBook(book);

            item.setQuantity(Integer.parseInt(qtyObj.toString()));

            BigDecimal price = new BigDecimal(priceObj.toString());
            BigDecimal qty   = new BigDecimal(qtyObj.toString());
            item.setTotalPrice(price.multiply(qty));
            item.setOrder(order); 
            order.addOrderItem(item);
        }

        // 4️⃣ حفظ في الداتابيز
        OrdersService service =
                new OrdersService(DBConnection.getConnection());
        service.placeOrder(order);

        JOptionPane.showMessageDialog(this,
                "Order placed successfully ✔");

        // 5️⃣ تنظيف / إغلاق
        this.dispose();

    } catch (Exception ex) {
        JOptionPane.showMessageDialog(this,
                "Error while placing order: " + ex.getMessage());
    }

